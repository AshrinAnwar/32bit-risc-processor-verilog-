`timescale 1ns / 1ps

module tb_top;

reg clk;
reg rst;
wire [15:0] led;

//////////////////////////////////////////////////
// Instantiate DUT (Device Under Test)
//////////////////////////////////////////////////

top DUT (
    .clk(clk),
    .rst(rst),
    .led(led)
);

//////////////////////////////////////////////////
// Clock Generation
//////////////////////////////////////////////////

initial
begin
    clk = 0;
end

always #5 clk = ~clk;   // 10ns clock period (100 MHz)

//////////////////////////////////////////////////
// Reset Sequence
//////////////////////////////////////////////////

initial
begin
    rst = 1;
    #20;
    rst = 0;
end

//////////////////////////////////////////////////
// Simulation Control
//////////////////////////////////////////////////

initial
begin

    $display("Starting Simulation");

    #500;

    $display("Processor Result (LED) = %d", led);

    $stop;

end

endmodule
